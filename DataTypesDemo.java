public class DataTypesDemo {
    public static void main(String[] args) {
        // To create any variable we use syntax : datatype variable_name;
        int num=10;
        float dec1=12.3f;
        char alpha='a';
        double dec2=18.22;
        boolean val=true;
        long l1=12333;
        short s1=23;

        System.out.println("Integer is : " + num);
        System.out.println("Float is : "+dec1);
        System.out.println("Double is : "+ dec2);
        System.out.println("Char is : " + alpha);
        System.out.println("BoOLEAN IS :" +val);
        System.out.println("Long is : "+l1);
        System.out.println("Short is : "+s1);
    }
}
